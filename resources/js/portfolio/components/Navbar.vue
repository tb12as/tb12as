<template>
  <nav
    class="bg-white border-gray-200 dark:bg-[#111] fixed top-0 right-0 w-full z-10"
  >
    <div
      class="max-w-screen-xl flex flex-wrap items-center justify-between mx-auto px-10 p-4"
    >
      <a href="https://tb12as.my.id/" class="flex items-center">
        <span
          class="self-center text-2xl font-semibold whitespace-nowrap dark:text-white"
          >Syafiq A.</span
        >
      </a>
      <button
        data-collapse-toggle="navbar-default"
        type="button"
        class="inline-flex items-center p-2 ml-3 text-sm text-gray-500 rounded-lg md:hidden hover:bg-gray-100 focus:outline-none focus:ring-2 focus:ring-gray-200 dark:text-gray-400 dark:hover:bg-gray-700 dark:focus:ring-gray-600"
        aria-controls="navbar-default"
        aria-expanded="false"
        @click.prevent="open = !open"
      >
        <span class="sr-only">Open main menu</span>
        <svg
          class="w-6 h-6"
          aria-hidden="true"
          fill="currentColor"
          viewBox="0 0 20 20"
          xmlns="http://www.w3.org/2000/svg"
        >
          <path
            fill-rule="evenodd"
            d="M3 5a1 1 0 011-1h12a1 1 0 110 2H4a1 1 0 01-1-1zM3 10a1 1 0 011-1h12a1 1 0 110 2H4a1 1 0 01-1-1zM3 15a1 1 0 011-1h12a1 1 0 110 2H4a1 1 0 01-1-1z"
            clip-rule="evenodd"
          ></path>
        </svg>
      </button>
      <div
        class="w-full md:block md:w-auto"
        :class="{ hidden: !open }"
        id="navbar-default"
      >
        <ul
          class="font-medium flex flex-col p-4 md:p-0 mt-4 border border-gray-100 rounded-lg bg-gray-50 md:flex-row md:space-x-8 md:mt-0 md:border-0 md:bg-white dark:bg-[#222] md:dark:bg-[#111] dark:border-gray-700"
        >
          <li>
            <a
              href="#"
              class="block py-2 pl-3 pr-4 text-gray-800 rounded hover:bg-gray-100 md:hover:bg-transparent md:border-0 md:hover:text-gray-900 md:p-0 dark:text-gray-200 md:dark:hover:text-white dark:hover:bg-gray-700 dark:hover:text-white md:dark:hover:bg-transparent"
              @click="toggleTheme"
            >
              <vue-feather :type="icon"></vue-feather
            ></a>
          </li>
        </ul>
      </div>
    </div>
  </nav>
</template>

<script>
export default {
  data() {
    return {
      open: false,
      icon: "sun",
    };
  },

  mounted() {
    this.changeIcon();
  },

  methods: {
    changeIcon() {
      const theme = localStorage.theme || "light";
      if (theme == "light") {
        this.icon = "moon";
      } else {
        this.icon = "sun";
      }
    },

    toggleTheme() {
      this.open = !this.open;

      const html = document.querySelector("html");
      const theme = localStorage.theme || "light";

      if (theme == "light") {
        localStorage.theme = "dark";
        html.classList.add("dark");
        html.classList.remove("light");
      } else {
        localStorage.theme = "light";
        html.classList.add("light");
        html.classList.remove("dark");
      }
      this.changeIcon();
    },
  },
};
</script>
